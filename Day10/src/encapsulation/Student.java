package encapsulation;

public class Student {
    // private fields
    private int id;
    private String name;

    // getter & setter methods
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
